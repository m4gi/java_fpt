    private void btnNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblStore.getModel();
        StoreDB Store = new StoreDB();
        Object[] row = new Object[4];
        row[0] = tfStoreID.getText();
        row[1] = tfStore_Name.getText();
        row[2] = tfAddress.getText();
        if (isNumeric(tfPhone.getText()) == true) {
            row[3] = tfPhone.getText();
        } else {
            JOptionPane.showMessageDialog(null, "Phone Number is not valid!");
            return;
        }
        if (Store.newStore(tfStoreID.getText(), tfStore_Name.getText(), tfAddress.getText(), tfPhone.getText()) != 0) {
            model.addRow(row);
        } else {
            JOptionPane.showMessageDialog(null, "Add Stores Failure!");
        }
    }//GEN-LAST:event_btnNewActionPerformed

