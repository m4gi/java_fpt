    private void Show_Store() {
        ArrayList<Store> list = StoreDB.getAllStores();
        DefaultTableModel model = (DefaultTableModel) tblStore.getModel();
        Object[] row = new Object[5];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getSID();
            row[1] = list.get(i).getSName();
            row[2] = list.get(i).getSAddress();
            row[3] = list.get(i).getSPhone();
            model.addRow(row);
        }
    }

