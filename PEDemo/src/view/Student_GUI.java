    private void btnNewActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnNewActionPerformed
    {//GEN-HEADEREND:event_btnNewActionPerformed
        // TODO add your handling code here:
        if (valid())
        {
            String studentID = this.txtStudentId.getText();
            String lastName = this.txtLastName.getText();
            String firstName = this.txtFirstName.getText();
            String gender = this.buttonGroup1.getSelection().toString().equalsIgnoreCase("Male")?"M":"F";
            String phone = this.txtPhone.getText();
            Student stu = stdDB.getStudent(studentID);
            if (stu == null)
            {
                int check = stdDB.addStudent(studentID, lastName, firstName, gender, Integer.parseInt(phone));
                if (check != 0)
                {
                    JOptionPane.showMessageDialog(this, "Add Student successfully!");
                } else
                {
                    JOptionPane.showMessageDialog(this, "Add Student failed!");
                }
            } else
            {
                JOptionPane.showMessageDialog(this, "Duplicate key!");
            }
        }
    }//GEN-LAST:event_btnNewActionPerformed

