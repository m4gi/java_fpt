    public boolean valid()
    {
        String S;
        S = this.txtStudentId.getText().trim().toUpperCase();
        this.txtStudentId.setText(S);
        if (!S.matches("^DE\\d{6}$"))
        {
            JOptionPane.showMessageDialog(this, "Student ID must be in format DE + 6 digits");
            txtStudentId.requestFocus();
            return false;
        }

        if (stdDB.getStudent(S) != null)
        {
            JOptionPane.showMessageDialog(this, "This Student ID existed");
            txtStudentId.requestFocus();
            return false;
        }

        S = this.txtLastName.getText().trim().toUpperCase();
        this.txtLastName.setText(S);
        if (S.length() == 0)
        {
            JOptionPane.showMessageDialog(this, "Last name is not empty");
            txtLastName.requestFocus();
            return false;
        }

        S = this.txtFirstName.getText().trim().toUpperCase();
        this.txtFirstName.setText(S);
        if (S.length() == 0)
        {
            JOptionPane.showMessageDialog(this, "First name is not empty");
            txtFirstName.requestFocus();
            return false;
        }

        S = this.txtPhone.getText().trim().toUpperCase();
        this.txtPhone.setText(S);
        if (!S.matches("^0\\d{9}$"))
        {
            JOptionPane.showMessageDialog(this, "Please input Phone in format 0123456789");
            txtPhone.requestFocus();
            return false;
        }
        return true;
    }

