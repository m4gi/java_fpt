    private class Thread1 extends Thread
    {

        public Thread1(String name)
        {
            this.setName(name);
        }

        @Override
        public void run()
        {
            this.countDown();
        }

        public void countDown()
        {
            int i = 1;
            while (i <= 100)
            {
                lbCount1.setText("Remaining " + i + " seconds");
                try
                {
                    i++;
                    if (i == 100)
                    {
                        int ret = JOptionPane.showConfirmDialog(rootPane, "Do you want to exit", "Confirm", JOptionPane.YES_NO_OPTION);
                        if (ret == JOptionPane.YES_OPTION)
                        {
                            JOptionPane.showMessageDialog(rootPane, "Goodbye");
                            System.exit(1);
                        } else
                        {
                            i = 1;
                        }
                    }
                    Thread.sleep(500);
                } catch (InterruptedException e)
                {
                    System.out.println(e);
                }
            }
        }
    }
    
