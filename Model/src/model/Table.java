    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblBook.getModel();
        BookDB book = new BookDB();
        if (book.updateBook(tfBookID.getText(), tfTitle.getText(), tfAuthor.getText(), (String) comboxCategory.getSelectedItem().toString(), tfKeyword.getText()) != 0) {
            model.setRowCount(0);
            Show_Book();
            lMessage.setText("Upadate Books Successfully!");
        } else {
            lMessage.setText("Update Books Failure!");
            jOptionPane1.showMessageDialog(null, "Update Books Failure!");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblBook.getModel();
        BookDB obj = new BookDB();
        int i = tblBook.getSelectedRow();
        if (i >= 0) {
            if (obj.deleteBookByID(tfBookID.getText()) == 1) {
                lMessage.setText("Delete Books Successfully!");
                model.removeRow(i);
            }
        } else {
            lMessage.setText("Delete Book Failure!");
            jOptionPane1.showMessageDialog(null, "Delete Books Failure!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

