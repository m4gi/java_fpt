    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String text = tfSearch.getText();
        BookDB obj = new BookDB();
        lMessage.setText("");
        DefaultTableModel model = (DefaultTableModel) tblBook.getModel();

        model.getDataVector().removeAllElements();
        Object[] row = new Object[5];

        if (comboxSearch.getSelectedItem().toString().equals("Book ID")) {
            ArrayList<Book> list = obj.searchByBookID(text);
            row[0] = list.get(0).getBookID();
            row[1] = list.get(0).getTitle();
            row[2] = list.get(0).getAuthor();
            row[3] = list.get(0).getCategory();
            row[4] = list.get(0).getKeyword();
            model.addRow(row);
            lMessage.setText("Search Book By BookID Successfully!");

        } else if (comboxSearch.getSelectedItem().toString().equals("Title")) {
            ArrayList<Book> list = obj.searchByTitle(text);
            for (int i = 0; i < list.size(); i++) {
                row[0] = list.get(i).getBookID();
                row[1] = list.get(i).getTitle();
                row[2] = list.get(i).getAuthor();
                row[3] = list.get(i).getCategory();
                row[4] = list.get(i).getKeyword();
                model.addRow(row);
                lMessage.setText("Search Book By Title Successfully!");
            }
        } else if (comboxSearch.getSelectedItem().toString().equals("Author")) {
            ArrayList<Book> list = obj.searchByAuthor(text);
            for (int i = 0; i < list.size(); i++) {
                row[0] = list.get(i).getBookID();
                row[1] = list.get(i).getTitle();
                row[2] = list.get(i).getAuthor();
                row[3] = list.get(i).getCategory();
                row[4] = list.get(i).getKeyword();
                model.addRow(row);
                lMessage.setText("Search Book By Author Successfully!");
            }
        } else if (comboxSearch.getSelectedItem().toString().equals("Category")) {
            ArrayList<Book> list = obj.searchByCategory(text);
            for (int i = 0; i < list.size(); i++) {
                row[0] = list.get(i).getBookID();
                row[1] = list.get(i).getTitle();
                row[2] = list.get(i).getAuthor();
                row[3] = list.get(i).getCategory();
                row[4] = list.get(i).getKeyword();
                model.addRow(row);
            }
            lMessage.setText("Search Book By Category Successfully!");
        } else if (comboxSearch.getSelectedItem().toString().equals("Keyword")) {
            ArrayList<Book> list = obj.searchByKeyword(text);
            for (int i = 0; i < list.size(); i++) {
                row[0] = list.get(i).getBookID();
                row[1] = list.get(i).getTitle();
                row[2] = list.get(i).getAuthor();
                row[3] = list.get(i).getCategory();
                row[4] = list.get(i).getKeyword();
                model.addRow(row);
                lMessage.setText("Search Book By Keyword Successfully!");
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

