    private void btnNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblBook.getModel();
        BookDB book = new BookDB();
        Object[] row = new Object[5];
        row[0] = tfBookID.getText();
        row[1] = tfTitle.getText();
        row[2] = tfAuthor.getText();
        row[3] = (String) comboxCategory.getSelectedItem().toString();
        row[4] = tfKeyword.getText();
        if (book.addNewBook(tfBookID.getText(), tfTitle.getText(), tfAuthor.getText(), (String) comboxCategory.getSelectedItem().toString(), tfKeyword.getText()) != 0) {
            model.addRow(row);
            lMessage.setText("Add Books Successfully!");
        } else {
            lMessage.setText("Add Books Failure!");
            jOptionPane1.showMessageDialog(null, "Add Books Failure!");
        }
    }//GEN-LAST:event_btnNewActionPerformed

