    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        DefaultMutableTreeNode selNode = (DefaultMutableTreeNode) this.jTree.getSelectionPath().getLastPathComponent();
        DefaultMutableTreeNode newNode = new DefaultMutableTreeNode(this.jTextField1.getText());
        selNode.add(newNode);
        //Reload Tree
        DefaultTreeModel treeModel = (DefaultTreeModel) this.jTree.getModel();
        treeModel.reload();
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        DefaultMutableTreeNode selNode = (DefaultMutableTreeNode) this.jTree.getSelectionPath().getLastPathComponent();
        selNode.setUserObject(this.jTextField1.getText());
        //Reload Tree
        DefaultTreeModel treeModel = (DefaultTreeModel) this.jTree.getModel();
        treeModel.reload();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        DefaultMutableTreeNode selNode = (DefaultMutableTreeNode) this.jTree.getSelectionPath().getLastPathComponent();
        if (selNode != this.jTree.getModel().getRoot()) {
            DefaultTreeModel treeModel = (DefaultTreeModel) this.jTree.getModel();
            treeModel.removeNodeFromParent(selNode);
            treeModel.reload();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void jTreeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTreeMouseClicked
        TreeSelectionModel smd = this.jTree.getSelectionModel();
        if (smd.getSelectionCount() > 0) {
            DefaultMutableTreeNode selNode = (DefaultMutableTreeNode) this.jTree.getSelectionPath().getLastPathComponent();
            String infor = selNode.getUserObject().toString();
            this.jTextField1.setText(infor);
        }
    }//GEN-LAST:event_jTreeMouseClicked

