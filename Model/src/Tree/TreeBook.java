    private void createNodes() {
        DefaultMutableTreeNode top = new DefaultMutableTreeNode("Book");
        DefaultMutableTreeNode category = null;
        DefaultMutableTreeNode book = null;
        ArrayList<Book> listBook = BookDB.getAllBooks();
        ArrayList<Category> listCat = CategoryDB.getCategory();
        for (int i = 0; i < listCat.size(); i++) {
            category = new DefaultMutableTreeNode(listCat.get(i).getCatName());
            top.add(category);
            for (int j = 0; j < listBook.size(); j++) {
                if (listBook.get(j).getCategory().equals(listCat.get(i).getCatID())) {
                    book = new DefaultMutableTreeNode(listBook.get(j).getTitle());
                    category.add(book);
                }
            }
        }

        DefaultTreeModel treeModel = new DefaultTreeModel(top);
        this.jTree.setModel(treeModel);
    }

