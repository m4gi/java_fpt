    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String text = tfSearch.getText();
        EmployeeDB obj = new EmployeeDB();
        lMessage.setText("");
        DefaultTableModel model = (DefaultTableModel) tblEmployees.getModel();

        model.getDataVector().removeAllElements();
        Object[] row = new Object[5];

        if (cmbSearch.getSelectedItem().toString().equals("Employee ID")) {
            ArrayList<Employee> list = obj.searchByID(text);
            row[0] = list.get(0).getEmp_Id();
            row[1] = list.get(0).getLast_Name();
            row[2] = list.get(0).getFirst_Name();
            row[3] = list.get(0).getGender();
            row[4] = list.get(0).getEmail();
            model.addRow(row);
            lMessage.setText("Search Employee By EmployeeID Successfully!");

        } else if (cmbSearch.getSelectedItem().toString().equals("First Name")) {
            ArrayList<Employee> list = obj.searchByFirstName(text);
            for (int i = 0; i < list.size(); i++) {
                row[0] = list.get(0).getEmp_Id();
                row[1] = list.get(0).getLast_Name();
                row[2] = list.get(0).getFirst_Name();
                row[3] = list.get(0).getGender();
                row[4] = list.get(0).getEmail();
                model.addRow(row);
                lMessage.setText("Search Employee By Title Successfully!");
            }
        } else if (cmbSearch.getSelectedItem().toString().equals("Last Name")) {
            ArrayList<Employee> list = obj.searchByLastName(text);
            for (int i = 0; i < list.size(); i++) {
                row[0] = list.get(0).getEmp_Id();
                row[1] = list.get(0).getLast_Name();
                row[2] = list.get(0).getFirst_Name();
                row[3] = list.get(0).getGender();
                row[4] = list.get(0).getEmail();
                model.addRow(row);
                lMessage.setText("Search Employee By Author Successfully!");
            }
        } else if (cmbSearch.getSelectedItem().toString().equals("Gender")) {
            ArrayList<Employee> list = obj.searchByGender(text);
            for (int i = 0; i < list.size(); i++) {
                row[0] = list.get(0).getEmp_Id();
                row[1] = list.get(0).getLast_Name();
                row[2] = list.get(0).getFirst_Name();
                row[3] = list.get(0).getGender();
                row[4] = list.get(0).getEmail();
                model.addRow(row);
            }
            lMessage.setText("Search Employee By Category Successfully!");
        }
    }//GEN-LAST:event_btnSearchActionPerformed

