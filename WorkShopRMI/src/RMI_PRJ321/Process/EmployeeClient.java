    private void btnNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblEmployees.getModel();
        EmployeeDB em = new EmployeeDB();
        Object[] row = new Object[5];
        row[0] = tfEmp_Id.getText();
        row[1] = tfLastName.getText();
        row[2] = tfFirstName.getText();
        row[3] = (String) cmbGender.getSelectedItem().toString();
        row[4] = tfEmail.getText();
        if (em.addNewEmployee(tfEmp_Id.getText(), tfLastName.getText(), tfFirstName.getText(), (String) cmbGender.getSelectedItem().toString(), tfEmail.getText()) != 0) {
            model.addRow(row);
            lMessage.setText("Add Employees Successfully!");
        } else {
            lMessage.setText("Add Employees Failure!");
        }
    }//GEN-LAST:event_btnNewActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblEmployees.getModel();
        EmployeeDB em = new EmployeeDB();
        if (em.updateEmployees(tfEmp_Id.getText(), tfLastName.getText(), tfFirstName.getText(), (String) cmbGender.getSelectedItem().toString(), tfEmail.getText()) != 0) {
            model.setRowCount(0);
            Show_Data();
            lMessage.setText("Upadate Employee Successfully!");
        } else {
            lMessage.setText("Update Employees Failure!");
            JOptionPane.showMessageDialog(null, "Update Employee Failure!");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblEmployees.getModel();
        EmployeeDB obj = new EmployeeDB();
        int i = tblEmployees.getSelectedRow();
        if (i >= 0) {
            if (obj.deleteBookByID(tfEmp_Id.getText()) == 1) {
                lMessage.setText("Delete Employee Successfully!");
                model.removeRow(i);
            }
        } else {
            lMessage.setText("Delete Employee Failure!");
            JOptionPane.showMessageDialog(null, "Delete Employee Failure!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

